create table if not exists public.discount_codes (
  id bigint generated by default as identity primary key,
  code text not null check (code = upper(trim(code)) and code ~ '^[A-Z0-9_-]{3,32}$'),
  discount_type text not null check (discount_type in ('percentage', 'fixed')),
  discount_value integer not null check (discount_value > 0),
  active boolean not null default true,
  starts_at timestamptz,
  expires_at timestamptz,
  max_uses integer check (max_uses is null or max_uses > 0),
  times_used integer not null default 0 check (times_used >= 0),
  created_at timestamptz not null default now(),
  created_by uuid references auth.users(id) on delete set null,
  constraint discount_codes_date_order check (expires_at is null or starts_at is null or expires_at > starts_at),
  constraint discount_codes_percentage_max check (discount_type <> 'percentage' or discount_value <= 100)
);

create unique index if not exists discount_codes_normalized_code_idx
  on public.discount_codes (upper(code));

alter table public.discount_codes enable row level security;

grant select, insert, update, delete on public.discount_codes to authenticated;
grant usage, select on sequence public.discount_codes_id_seq to authenticated;

