create or replace function public.consume_discount_code(p_code text, p_original_price integer)
returns table (
  discount_code_id bigint,
  normalized_code text,
  discount_type text,
  discount_value integer,
  discount_amount integer,
  final_price integer
)
language plpgsql
security definer
set search_path = public, pg_temp
as $$
declare
  selected_code public.discount_codes%rowtype;
  calculated_discount integer;
begin
  if p_original_price is null or p_original_price <= 0 then
    raise exception 'invalid_original_price' using errcode = '22023';
  end if;

  select *
  into selected_code
  from public.discount_codes
  where upper(code) = upper(trim(p_code))
    and active = true
    and (starts_at is null or starts_at <= now())
    and (expires_at is null or expires_at > now())
    and (max_uses is null or times_used < max_uses)
  for update;

  if not found then
    raise exception 'invalid_discount_code' using errcode = 'P0001';
  end if;

  calculated_discount := case
    when selected_code.discount_type = 'percentage'
      then round(p_original_price * selected_code.discount_value / 100.0)::integer
    else selected_code.discount_value
  end;
  calculated_discount := least(greatest(calculated_discount, 0), greatest(p_original_price - 1, 0));

  update public.discount_codes
  set times_used = times_used + 1
  where id = selected_code.id;

  return query select
    selected_code.id,
    upper(trim(selected_code.code)),
    selected_code.discount_type,
    selected_code.discount_value,
    calculated_discount,
    p_original_price - calculated_discount;
end;
$$;

revoke all on function public.consume_discount_code(text, integer) from public;
revoke all on function public.consume_discount_code(text, integer) from anon;
revoke all on function public.consume_discount_code(text, integer) from authenticated;
grant execute on function public.consume_discount_code(text, integer) to service_role;
